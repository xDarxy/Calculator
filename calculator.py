number1 = float(input("Choose a number: "))
operator = input("choose an operator: ")
number2 = float(input("Choose a number: "))

if (operator == "+"):
    print(number1 + number2)

if (operator == "-"):
    print(number1 - number2)

if (operator == "*"):
    print(number1 * number2)

if (operator == "/"):
    print(number1 / number2)